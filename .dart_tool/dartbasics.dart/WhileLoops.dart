void main() {
//   // For IN LOOP:

  String value = 'Hello';
  for (int i = 0; i < value.length; i++) {
    print(value[i]);
  }

//   // WHILE LOOP:
//   // In while loop you have a condition and it'll only run till the condition is true

  int i = 0;

  while (i < value.length) {
    print(value[4]);
    i++;
  }

